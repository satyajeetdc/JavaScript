// while
let index = 0;
while (index <= 10) {
  console.log(index);
  index += 2;
}

let myArr = ["Flash", "Superman", "Batman"];
let arr = 0;
while (arr < myArr.length) {
  console.log(myArr[arr]);
  arr++;
}

// do-while
let score = 1;
do {
  console.log(score);
  score++;
} while (score <= 5);

let score1 = 11;
do {
  console.log(score1);
  score1++;
} while (score1 <= 5);
